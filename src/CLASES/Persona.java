package CLASES;


import java.util.*;

/**
 * 
 */
public class Persona {

    public String Nombre;
    public String Apellido;
    public String CI;
    public String User;
    public String Pass;
    
    public Persona() {
    }

    public Persona(String Nombre, String Apellido, String CI, String User, String Pass) {
        this.Nombre = Nombre;
        this.Apellido = Apellido;
        this.CI = CI;
        this.User = User;
        this.Pass = Pass;
    }

    

}